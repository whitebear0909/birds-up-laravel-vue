The name of the app was affectionately derived from the repeated declaration of the phrase “Birds UP!” as birds flushed wild during quail hunts in the Red Hills region. The Birds Up app was designed and tested by quail hunters, and it was additionally vetted and beta-tested for an entire hunting season (2013-14) by local plantation landowners and managers in the Red Hills region. It is still very much a work in progress and we look forward to hearing from you on ways we can improve the app and better serve your needs.</p>
