        <p class="text-center font-italic font-weight-bold">Many southern hunting plantations, and ranches out West, have collected quail hunting information such as covey finds for dozens of years. Traditional means of collecting these data has, to date, consisted of mostly paper records using hunt cards, hunt slips, or notebooks. During the past decade, land owners and land managers have begun to convert these data from paper format stored in file cabinets to digital format using computers and applications such as MS Excel. Yet, entering this data is both labor-intensive and time-consuming; for many properties this data entry requires several days to even several weeks at the end of each season which may delay summarization, evaluation and analysis. However, the advent of smart phones and mobile devices has made it easier than ever to collect this type of data in the field as well as collect additional information such as locational position on-the-fly.
